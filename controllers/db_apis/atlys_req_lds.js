const database = require ('../../services/database');

const baseQuery =

`SELECT 'ATLYS_REQ_LDS' TABELA, SYSDATE OPERACAO, 
TELEFONICA.ATLYS_REQ_LDS.IDS_ATLYS_REQ_LDS ,
TELEFONICA.ATLYS_REQ_LDS.CEP ,
TELEFONICA.ATLYS_REQ_LDS.DOCUMENTO ,
TELEFONICA.ATLYS_REQ_LDS.DTNASC ,
TELEFONICA.ATLYS_REQ_LDS.SYS_CREATEDATE ,
TELEFONICA.ATLYS_REQ_LDS.USUARIO ,
TELEFONICA.ATLYS_REQ_LDS.DDD ,
TELEFONICA.ATLYS_REQ_LDS.UF from ATLYS_REQ_LDS INNER JOIN DADOSPROPOSTADS ON  ATLYS_REQ_LDS.IDS_ATLYS_REQ_LDS = DADOSPROPOSTADS.IDS_ATLYS_REQ_LDS  and DADOSPROPOSTADS.NUMPROPOSTA = NUMPROPOSTA`;


