const database = require ('../../services/database');

const baseQuery =

`SELECT 'ATLYS_RESP_LDS' TABELA, 'INSERT' OPERACAO, 
TELEFONICA.ATLYS_RESP_LDS.IDS_ATLYS_RESP_LDS ,
TELEFONICA.ATLYS_RESP_LDS.CODIGO_RETORNO ,
TELEFONICA.ATLYS_RESP_LDS.DESC_RESTRICAO ,
TELEFONICA.ATLYS_RESP_LDS.FLAG_RESTRICAO ,
TELEFONICA.ATLYS_RESP_LDS.MENSAGEM ,
TELEFONICA.ATLYS_RESP_LDS.SCORE ,
TELEFONICA.ATLYS_RESP_LDS.SYS_CREATEDATE from ATLYS_RESP_LDS INNER JOIN DADOSPROPOSTADS ON  ATLYS_RESP_LDS.IDS_ATLYS_RESP_LDS = DADOSPROPOSTADS.IDS_ATLYS_RESP_LDS  and DADOSPROPOSTADS.IDS_DADOSPROPOSTADS = IDS_DADOSPROPOSTADS`;

