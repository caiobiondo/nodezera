const database = require ('../../services/database');

const baseQuery =

`SELECT 'CALLCENTER_REQ_LDS' TABELA, 'INSERT' OPERACAO, 
CALLCENTER_REQ_LDS.IDS_CALLCENTER_REQ_LDS ,
CALLCENTER_REQ_LDS.CEP ,
CALLCENTER_REQ_LDS.DOCUMENTO ,
CALLCENTER_REQ_LDS.DTNASC ,
CALLCENTER_REQ_LDS.SYS_CREATEDATE ,
CALLCENTER_REQ_LDS.USUARIO from CALLCENTER_REQ_LDS INNER JOIN DADOSPROPOSTADS ON  CALLCENTER_REQ_LDS.IDS_CALLCENTER_REQ_LDS = DADOSPROPOSTADS.IDS_CALLCENTER_REQ_LDS  and DADOSPROPOSTADS.IDS_DADOSPROPOSTADS = IDS_DADOSPROPOSTADS `;

