const database = require ('../../services/database');

const baseQuery =

`SELECT 'CALLCENTER_RESP_LDS' TABELA, 'INSERT' OPERACAO, 
CALLCENTER_RESP_LDS.IDS_CALLCENTER_RESP_LDS ,
CALLCENTER_RESP_LDS.CODIGO_COR ,
CALLCENTER_RESP_LDS.CODIGO_PLANO ,
CALLCENTER_RESP_LDS.CODIGO_RETORNO ,
CALLCENTER_RESP_LDS.DESC_RESTRICAO ,
CALLCENTER_RESP_LDS.FAIXA_CEP ,
CALLCENTER_RESP_LDS.FLAG_RESTRICAO ,
CALLCENTER_RESP_LDS.MENSAGEM ,
CALLCENTER_RESP_LDS.RECOMENDACAO_POLITICA ,
CALLCENTER_RESP_LDS.SCORE ,
CALLCENTER_RESP_LDS.SYS_CREATEDATE from CALLCENTER_RESP_LDS INNER JOIN DADOSPROPOSTADS ON  CALLCENTER_RESP_LDS.IDS_CALLCENTER_RESP_LDS = DADOSPROPOSTADS.IDS_CALLCENTER_RESP_LDS  and DADOSPROPOSTADS.IDS_DADOSPROPOSTADS = IDS_DADOSPROPOSTADS`;


