const database = require ('../../services/database');

const baseQuery =

`SELECT 'CONSULTACREDITO_REQ' TABELA, 'INSERT' OPERACAO, 
CONSULTACREDITO_REQ.IDS_CONSULTACREDITO_REQ ,
CONSULTACREDITO_REQ.CEP ,
CONSULTACREDITO_REQ.DATANASCIMENTO ,
CONSULTACREDITO_REQ.DOCUMENTO ,
CONSULTACREDITO_REQ.NOMECLIENTE ,
CONSULTACREDITO_REQ.NOMEMAE ,
CONSULTACREDITO_REQ.SENHAGESTO ,
CONSULTACREDITO_REQ.SENHASERASA ,
CONSULTACREDITO_REQ.SYS_CREATEDATE ,
CONSULTACREDITO_REQ.USUARIOGESTOR ,
CONSULTACREDITO_REQ.USUARIOSERASA from CONSULTACREDITO_REQ INNER JOIN DADOSPROPOSTADS ON  CONSULTACREDITO_REQ.IDS_CONSULTACREDITO_REQ = DADOSPROPOSTADS.IDS_CONSULTACREDITO_REQ  and DADOSPROPOSTADS.IDS_DADOSPROPOSTADS = IDS_DADOSPROPOSTADS `;

